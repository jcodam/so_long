int	ft_isalnum(int c)
{
	int	i;

	if (c >= 'a' && c <= 'z')
		i = 1;
	else if (c >= 'A' && c <= 'Z')
		i = 2;
	else if (c >= '0' && c <= '9')
		i = 3;
	else
		i = 0;
	return (i);
}
